package Exercise_1.Structural.Universal_Remote_Control;

public class SamsungTV {
    public void turnOn() {
        System.out.println("Samsung TV is ON.");
    }

    public void turnOff() {
        System.out.println("Samsung TV is OFF.");
    }

    public void increaseVolume() {
        System.out.println("Samsung TV volume increased.");
    }

    public void decreaseVolume() {
        System.out.println("Samsung TV volume decreased.");
    }
}
